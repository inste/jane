#include "error.h"

void error(char * module, char * error_msg) {
	printf("[%s] Error: %s\n", module, error_msg);
	exit(-1);
}



void warning(char * module, char * warn_msg) {
	printf("[%s] Warning: %s\n", module, warn_msg);
}

